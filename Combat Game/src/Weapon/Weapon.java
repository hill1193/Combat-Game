package Weapon;

public abstract class Weapon {
	int attack;
	int defense;
	int speed;
	int stamina;
	String name;
	boolean twohand; // true for two handed weapon, false for one handed weapon
	
	public int getAttack() {
		return this.attack;
	}
	
	public int getDefense() {
		return this.defense;
	}
	
	public int getSpeed() {
		return this.speed;
	}
	
	public int getStamina() {
		return this.stamina;
	}
	
	public String getName() {
		return this.name;
	}
	
	public boolean getTwoHand() {
		return this.twohand;
	}
}
