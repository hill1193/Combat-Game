package Combatant;
import Armor.*;
import Weapon.*;

public class Equipment {
	Armor head;
	Armor chest;
	Armor wrist;
	Armor hand;
	Armor leg;
	Armor feet;
	Armor neck;
	Armor finger;
	Weapon mainhand;
	Weapon offhand;
	
	public Equipment(Armor head, Armor chest, Armor wrist, Armor hand, Armor leg, Armor feet, Armor neck, Armor finger) {
		head = null;
		chest = null;
		wrist = null;
		hand = null;
		leg = null;
		feet = null;
		neck = null;
		finger = null;
	}
	
	public int getBonusAtt() {
		return this.head.getAttack();
	}
	
	public int getBonusDef() {
		return this.head.getDefense();
	}
	
	public int getBonusSpd() {
		return this.head.getSpeed();
	}
	
	public int getBonusStam() {
		return this.head.getStamina();
	}
		
}
