package Armor;

public abstract class Armor {
	int attack;
	int defense;
	int speed;
	int stamina;
	String name;
	
	public int getAttack() {
		return this.attack;
	}
	
	public int getDefense() {
		return this.defense;
	}
	
	public int getSpeed() {
		return this.speed;
	}
	
	public int getStamina() {
		return this.stamina;
	}
	
	public String getName() {
		return this.name;
	}
	
}
